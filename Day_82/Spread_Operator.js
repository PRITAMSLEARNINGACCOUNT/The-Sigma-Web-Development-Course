console.log("In JavaScript Spread Operator Looks Like '...' It Is Used To Convert An Iterable/Array Into An Object.");
let array = ["Value_1", "Value_2", "Value_3"];
console.log("So The Array Declared For Demonstration Of Spread Operator Looks Like " + array);
const object = { ...array };
console.log("And The Object Created From That Array Using Spread Operator Now Looks Like " + object);